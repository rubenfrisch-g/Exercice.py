# 1) What is a module?
# A module is a Python file that contains code such as functions, variables, or classes.

# 2) List three ways to import a module in Python
# 1. import math
# 2. from math import sqrt
# 3. import math as m

# 3) What is the purpose of importing?
# Importing allows you to use code that was written in another module,
# so you do not have to rewrite functions yourself.

# 4) List three examples when you would use the random module
# 1. Generate a random number in a game: random.randint(1, 6)
# 2. Shuffle a list: random.shuffle(cards)
# 3. Pick a random item: random.choice(names)

# 5) What is an ImportError?
# An ImportError happens when Python cannot find or load a module you are trying to import.

# 6) When would using an OrderedDict be useful?
# When you need a dictionary that keeps the order of inserted items,
# for example when tracking events or maintaining a specific order.

# 7) When would using a defaultdict be useful?
# When you want a dictionary that automatically creates default values,
# such as counting items or grouping values by keys.

# 8) What is the purpose of the following code?
# if __name__ == '__main__':
#     pass
#
# It means the code inside this block will only run if the file is executed directly,
# not when it is imported into another file.
# The "pass" statement does nothing; it is just a placeholder.
